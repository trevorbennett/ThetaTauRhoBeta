									<h3 style="font-size:20pt"><strong>Alumni Involvement</strong></h3>
                                    </header>
                                    <p>The growth, success, and stability of Theta Tau are due to the outstanding support given by our alumni members. Our alumni continue to carry out the purposes of the Fraternity and show their Theta Tau pride.</p>
                                    <p>Theta Tau’s opportunities for involvement and participation are many. Among the most satisfying alumnus opportunities are mentoring student members, serving as advisers, assisting with recruitment, being a house corporation member and interacting at special occasions, all of which assist our student members and strengthen the chapter. Alumni can also be involved in alumni clubs and national committees. Alumni are not, however, involved in the day-to-day operation of the active student chapter.</p>
                                    
                                    <p>Our alumni are appreciated for whatever contribution they are able to make to the Fraternity on formal or informal basis. Involvement may include contributions of time, talent or treasure. Founders' Day celebrations, initiations, homecoming, Engineers Week, job fairs, and anniversary celebrations for chapters often bring alumni together with students, emphasizing that Theta Tau is truly for a lifetime.</p>

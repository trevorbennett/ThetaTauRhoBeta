										<h3 style="font-size:24pt"><strong>History</strong></h3>
									</header>
									<p><strong>Theta Tau Fraternity </strong>was founded in 1904 by four engineering students at the University of Minnesota in Minneapolis. As defined by the fraternity, the purpose of Theta Tau is to develop and maintain a high standard of professional interest among its members, and to unite them in a strong bond of fraternal fellowship. The goals of the fraternity are to promote the social and professional development of its members during and after their college years. Today, Theta Tau is the oldest and largest professional engineering fraternity in the United States, with a diverse membership of men and women studying engineering at more than 40 campuses.</p>
                                    <img src="images/ThetaTauFounders.jpg" id="founders"/>
									<p>The Fraternity was founded as the “Society of Hammer and Tongs” on October 15, 1904, by Erich Julius Schrader, Elwin Leroy Vinal, William Murray Lewis, and Isaac Baker Hanks. The Fraternity’s principal founder, Erich Schrader, dreamed of establishing an engineering fraternity similar to those already existing in law, medicine, and dentistry. The name was changed to Theta Tau at the fraternity’s first national convention at the University of Minnesota in 1911. Since then, approximately 30,000 members have been initiated. </p>
                                    <p>Brother Schrader was chiefly responsible for the Ritual, Constitution, and the Bylaws adopted by the founders. The first badge was a gold skull with the letters Θ and Τ on its forehead, and a crossed hammer and tongs beneath. The Constitution provided for the establishment of additional chapters at other leading engineering schools, and the Fraternity soon began to take on its national character.</p>
